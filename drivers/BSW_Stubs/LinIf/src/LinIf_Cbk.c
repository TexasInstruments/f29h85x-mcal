Std_ReturnType LinIf_CheckWakeup(EcuM_WakeupSourceType WakeupSource)
{
    Std_ReturnType return_value = E_NOT_OK;
    uint8 channel_id;

    if (WakeupSource == 1U)
    {
        /* Wakeup Source 1 configured for Channel 0 */
        channel_id = 0U;
    }
    else
    {
        channel_id = 1U;
    }
    
    return_value = Lin_CheckWakeup(channel_id);

    return return_value;
}

